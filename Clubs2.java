public class Clubs2 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs2()
    {
      number=2;
      suit="Clubs";
      name="2 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}