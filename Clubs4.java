public class Clubs4 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs4()
    {
      number=4;
      suit="Clubs";
      name="4 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}