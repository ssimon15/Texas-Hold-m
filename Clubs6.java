public class Clubs6 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs6()
    {
      number=6;
      suit="Clubs";
      name="6 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}