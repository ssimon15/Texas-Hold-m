public class Clubs3 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs3()
    {
      number=3;
      suit="Clubs";
      name="3 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}