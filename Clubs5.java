public class Clubs5 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs5()
    {
      number=5;
      suit="Clubs";
      name="5 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}