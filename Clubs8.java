public class Clubs8 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs8()
    {
      number=8;
      suit="Clubs";
      name="8 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}