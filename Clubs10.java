public class Clubs10 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs10()
    {
      number=10;
      suit="Clubs";
      name="10 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}