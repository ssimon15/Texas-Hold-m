public class Clubs9 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs9()
    {
      number=9;
      suit="Clubs";
      name="9 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}