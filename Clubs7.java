public class Clubs7 implements Card
{
  private int number;
  private String suit;
  private String name;
    Clubs7()
    {
      number=7;
      suit="Clubs";
      name="7 of Clubs";
    }
    public int getnumber()
    {
      return number;
    }
    public String getsuit()
    {
      return suit;
    }
    public String getname()
    {
      return name;
    }
}